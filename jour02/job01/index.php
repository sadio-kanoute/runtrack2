<?php
/* for est un boucle qui permet de répéter une action un nombre de fois défini ou cest combien de iterations on 
doit faire  le ++ est un opérateur d'incrémentation qui augmente la valeur de la variable de 1 à chaque itération. 
les if et else les conditions */
for ($i = 0; $i <= 1337; $i++) {
    if ($i == 42) {
        echo "<b><u>$i</u></b><br />";
    } else {
        echo "$i<br />";
    }
}
?>
